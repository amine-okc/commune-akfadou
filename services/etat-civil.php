						<h1>Acte de naissance</h1><br><br>
						<section>
							<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;La naissance de l’enfant est déclarée par le père ou la mère ou par les docteurs en médecine, les sages femmes ou autres personnes qui ont assisté à l’accouchement.</p>
							<br>&nbsp;&nbsp;&nbsp;&nbsp;<b>La constitution du dossier</b><br>

							Le déclarant doit présenter le livret de famille, et le cas échéant, les actes de naissance du père et de la mère et l’acte de leur mariage.



							<br><br>&nbsp;&nbsp;&nbsp;&nbsp;<b>Le choix des prénoms</b><br>

							Les prénoms sont choisis par le père ou la mère et en leur absence par le déclarant. sont interdits tous les prénoms autres que ceux consonances par l’usage ou par la tradition.

							Les prénoms doivent être de consonance algérienne pour les enfants nés de parents musulmans. Ils peuvent ne pas l’être dans le cas de parents non musulmans.
							<br><br>&nbsp;&nbsp;&nbsp;&nbsp;<b>Délai de déclaration</b><br>

							<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Les déclarations de naissance sont faites dans les cinq jours de l’accouchement à l’officier de l’état civil du lieu de naissance. Dans les wilayas de la Saoura et des oasis les déclarations sont faites dans les vingt (20) jours de l’accouchement.</p>

							&nbsp;&nbsp;&nbsp;&nbsp;<b>Le respect des délais</b><br>

							<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Lorsqu’une naissance n’a pas été déclarée dans le délai légal, l’officier de l’état civil ne peut la relater sur ses registres qu’en vertu d’une ordonnance rendue par le président du tribunal de l’arrondissement dans lequel est né l’enfant, et mention sommaire est faite en marge à la date de la naissance.

								Le non-respect de ces délais cinq (5) jours dans tout le territoire national et les vingt (20) jours dans les wilayas de la Saoura et des Oasis exige l’application des sanctions prévues à <a href="../assets/img/art442.png" style="color : black" target="__blank">&nbsp;&nbsp;&nbsp;&nbsp;<b>l’article 442(3) du code pénal</b></a>. </p>

							<br>&nbsp;&nbsp;&nbsp;&nbsp;<b>La modification des actes de l’état civil</b><br><br>
							<ul>
								<li>Tout changement de nom ou de prénom donne lieu à rectification des actes de l’état civil par le tribunal.</li>
								<li>Les prénoms figurant dans l’acte de naissance peuvent en cas d’intérêt légitime être modifié par ordonnance du président de tribunal prononcée sur réquisition du procureur de la république saisi soit à la requête de l’intéressé, soit de son représentant légal, s’il est mineur.</li>
								<li>Il peut être procédé, sans frais, par voie de simple ordonnance rendue par le président du tribunal de l’arrondissement judiciaire du lieu ou l’acte a été dressé ou transcrit. sur requête du procureur de la république, à toute rectification des actes de l’état civil ou décisions judiciaires les concernant.</li>
								<li>Le requérant saisit le procureur de la république par simple requête sur papier libre.</li>
								<li>Le procureur de la république, présente la requête en rectification.</li>
							</ul>



