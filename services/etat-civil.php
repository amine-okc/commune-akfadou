						<h1>Extrait de naissance 12-S</h1><br><br>
						<section>
							<p>
								L’Acte de Naissance Spécial 12S est un document officiel contenant des informations d’état civil permettant la génération et l’attribution d’un Numéro d’identifiant National unique à chaque citoyen.

								<br><br>Le 12S est un document authentique :
								<ul>
									<li>Il est délivré par la commune de naissance sur la base du registre d’Etat Civil des naissances.</li>
									<li>Il est établi par le biais d’une plateforme informatique sécurisée et suivant des procédures strictes de validation hiérarchique.</li>
								</ul>
								Le 12S est un document sécurisé :
								<ul>

									<li>Il est édité sur un support papier fiduciaire comportant le sceau de l’état en filigrane, des impressions de sécurité et une bande métallique à couleurs variables intégrée dans le papier.</li>
									<li>Il comporte un numéro de série unique établissant la traçabilité du document et permettant de détecter toute reproduction. </li>
								</ul>

							</p>
						</section>
