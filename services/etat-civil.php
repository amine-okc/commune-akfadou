						<h1>Acte de mariage</h1><br><br>
						<section>

							L’un et l’autre, des futurs époux doivent justifier de leur état civil par la production de l’un des documents suivants :
							<ul>
								<li class="deco-none">1 - Extrait datant de moins de trois (3) mois, soit de l’acte de naissance, soit de la transcription du jugement individuel ou collectif de naissance.</li>

								<li class="deco-none">2 - fiche de résidence de l’un des époux.</li>

								<li class="deco-none">3 - Certificat médical des deux époux.</li>

								<li class="deco-none">4 - Et selon le cas :</li>
								<ul>

									<li> L’autorisation du mariage pour les militaires et corps de sécurité.</li>

									<li> La dispense de la condition d’âge pour les mineurs.</li>

									<li> Le livret de famille relatif à un précédent mariage. </li>

									<li> La femme dont le précédent mariage a été dissous, doit produire:
										<ul>

											<li class="deco-none">- Soit l’acte de décès du précédent mari ou l’acte de naissance de celui-ci portant mention du décès , ou bien le livret de famille ou figure l’acte de décès.</li>

											<li class="deco-none">- Soit un extrait de l’acte de mariage ou de naissance portant mention de la dissolution du mariage ou le livret de famille ou figure cette mention ou bien encore, la décision de dissolution accompagnée d’une attestation du magistrat ou du greffier compétent certifiant qu’elle est devenue définitive.</li>
									</li>
								</ul>
							</ul>
							<li class="deco-none">5 - Les deux témoins doivent présenter leurs carte nationale d’identité ou n’importe quel document prouvant les identifiés.</li>
							</ul><br>
							<div class="alert alert-info">L’acte de mariage peut être retiré auprès de n’importe quelle commune ou annexe administrative (pas nécessairement là où il a été transcrit).</div>
							<div class="alert alert-success"><b style="font-size : 17pt">Demande en ligne</b>
