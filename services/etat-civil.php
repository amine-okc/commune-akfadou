						<h1>Fiche familiale d'état civil</h1><br><br>
						<section>
							<b>L’administration concernée</b><br><br>

							<p>Pour l'obtention de la fiche familiale d'état civil, le demandeur doit se déplacer aux services de l'état civil du lieu de sa résidence.</p><br>
							<b>Les documents demandés</b><br><br>

							<p>La fiche familiale d'état civil se délivre au vu du livret de famille, sans demander d'autres documents, cette condition est justifiable que la fiche familiale d'état civil constitue en même temps :<br>
								<ul>
									<li>Un extrait de naissance des époux et leurs enfants</li>
									<li>Certificat de vie</li>
									<li>Acte de mariage et certificat de divorce des époux et, le cas échéant,  des actes de décès.</li>
								</ul><br><br>
								<div class="alert alert-info">

									<ul>
								<li style="list-style : circle">Comme une règle générale, la délivrance de la fiche familiale d'état civil se fait immédiatement.</li>
								<li style="list-style : circle">La durée de validité de la fiche familiale est d'une (01) année.</li>
									</ul>
							</div>
							</p>
						</section>
