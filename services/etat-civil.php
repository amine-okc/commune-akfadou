						<h1>Acte de décès</h1><br><br>

						<section>
							<p>
								&nbsp;&nbsp;&nbsp;&nbsp;<b>L’administration concernée</b><br>

								L'acte de décès est dressé par l'officier de l'état civil de la commune.<br><br>

								&nbsp;&nbsp;&nbsp;&nbsp;<b>Les personnes habilitées à la déclaration</b><br>

								La déclaration de décès se fait par l'un des personnes suivantes :
								<ul>

									<li>Parent de défunt</li>

									<li>Personne possédant sur son état civil les renseignements les plus exacts et les plus complets possibles.</li>

									<li>Les hôpitaux ou les structures de santé ou les hôpitaux maritimes.</li>

									<li>Les responsables des établissements pénitentiaires, …Etc.</li>
								</ul>

								&nbsp;&nbsp;&nbsp;&nbsp;<b>Les délais de la déclaration</b><br>

								Les déclarations de décès doivent être faites dans un délai de vingt quatre (24) heures à compter du décès.<br>





								Pour les habitants des wilayas du sud, la déclaration du décès est de vingt (20) jours.<br><br>

								&nbsp;&nbsp;&nbsp;&nbsp;<b>Les cas exceptionnels</b><br>
								<ul>

									<li>Lorsqu’un décès n’a pas été déclaré dans le délai légal, l’officier de l’état civil ne peut la relater sur ses registres qu’en vertu d’une ordonnance rendue par le président du tribunal de l’arrondissement dans lequel est né l’enfant, et mention sommaire est faite en marge à la date de la naissance.</li>

									<li>L'inobservance de ce délai imparti aux personnes entraîne l'application des peines prévues à l'article 441 alinéas 02 du code pénal.</li>

									<li>La déclaration de décès même tardive est reçue et l'acte de dresser quoi que soit le temps écouler depuis le décès des lors qu'elle peut encore être vérifiée par l'examen du corps. </li>
								</ul>
								&nbsp;&nbsp;&nbsp;&nbsp;<b>Les documents demandés</b><br>

								L'officier de l'état civil ne peut délivrer l'acte de décès que sur production d'un certificat établi par un médecin ou, à défaut, par l'officier de police judiciaire qu'il a chargé de s'assurer du décès.

								La présentation du livret famille et, le cas échéant, un extrait de naissance du défunt.



								La délivrance d'un extrait de décès

								Le demandeur de l'extrait de décès a le droit de choisir entre sa présence lui-même au service de l'état civil de la commune, lieu de décès ou l'envoi d'une demande manuscrite par voie postale :

								<li>Le premier cas : Il doit lui identifier en précisant le degré de parenté avec la personne décédée, et fournir tous les renseignements qui faciliteront la recherche.

								<li>Le deuxième cas : Il doit envoyer une demande dans une enveloppe timbrée et libellée à l'adresse du demandeur ; comporte le nom, le prénom, la date et lieu du décès du défunt et la filiation.


									<br><br><br>
