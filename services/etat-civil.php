										<i>
											<ul style="list-style: circle">
												<li>La durée de validité de l'acte de décès est illimitée.</li><br>



												<li>L’acte de décès peut être retiré auprès de n’importe quelle commune ou annexe administrative (pas nécessairement là où il a été transcrit).</li>
										</i></ul>
									</div>
