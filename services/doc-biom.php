                        <h1>Passeport biométrique</h1><br><br>
                        <section>
                            <b style="font-size : 17pt">Dépôt de dossier</b><br><br>
                            <p>
                                Le passeport biométrique Algérien, qui est conforme aux normes internationales définies par l’OACI Doc9303, a été mis en circulation depuis le 04 janvier 2012, au niveau de 48 sites pilotes. Il a ensuite été généralisé à l’ensemble des Circonscriptions Administratives, des Daïras et des Postes Diplomatiques et Consulaires d’Algérie à l’étranger. Les passeports ordinaires non biométriques, ne seront plus en circulation à compter du 24 novembre 2015.

                                <br><br>Le dépôt du dossier de demande du passeport biométrique électronique s’effectue auprès de la circonscription administrative, de la daïra ou du service consulaire du lieu de résidence.

                                <br><br>Le dépôt confirmé du dossier et l’enrôlement des données biométriques donnent lieu à la délivrance d’un récépissé par le responsable habilité.
                            </p>
                            <br><br>
                            <b style="font-size : 17pt">Retrait du passeport biométrique</b><br><br>
                            <p>
                                Le retrait du document demandé s’effectue par le demandeur lui-même, à la date de délivrance préalablement fixée.



                                <br><br>A l’occasion du retrait, il est vérifié la conformité des informations alphanumériques personnelles imprimées sur le document, en présence du demandeur.



                                <br><br>Le document demandé est remis à son titulaire, contre signature d’un accusé de réception.



                                <br><br>Le numéro d’identification nationale unique définitif « NIN » inscrit sur le document demandé est communiqué à l’intéressé. Ce numéro est également porté sur l’extrait d’acte de naissance spécial n°12-S, qui lui est restitué au moment du retrait.
                            </p><br><br>
                            <p>
                                <b style="font-size : 17pt">Dossier relatif au passeport biométrique</b><br><br>
                                Le dossier de demande du passeport biométriques électroniques comprend :

                                <br><br><b>Pour les demandeurs majeurs résidant en Algérie :</b>
                                <ul>
                                    <li style="list-style: none;">1. l’extrait d’acte de naissance spécial n°12-S de l’intéressé, délivré sur imprimé spécial.</li>
                                    <li style="list-style: none;">2. le certificat de nationalité algérienne, lorsque la demande est exprimée pour la première fois.</li>
                                    <li style="list-style: none;">3. le passeport parvenu à expiration, accompagné de l’acte de naissance du père ou de la mère, ou à défaut, l’acte de décès de l’un des deux parents, lorsqu’il s’agit d’une demande de renouvellement.</li>
                                    <li style="list-style: none;">4. le certificat de résidence datant de moins de six (6) mois.</li>
                                    <li style="list-style: none;">5. l’attestation de travail ou le certificat de scolarité pour les étudiants.</li>
                                    <li style="list-style: none;">6. quatre (4) photos d’identité en couleur, numériques, récentes et identiques (le fond de la photo biométrique doit être uni, sans contour et de couleur blanche).</li>
                                    <li style="list-style: none;">7. une quittance fiscale ou timbre fiscal dont le montant correspond à la nature du document demandé.</li>
                                    <li style="list-style: none;">8. la copie de la carte du groupe sanguin.</li>
                                </ul><br>

                                <b>Pour les demandeurs mineurs résidant en Algérie :</b>
                                <ul>
                                    <li style="list-style: none;">1. l’extrait d’acte de naissance spécial n°12-S de l’intéressé, délivré sur imprimé spécial.</li>
                                    <li style="list-style: none;">2. le certificat de nationalité algérienne.</li>
                                    <li style="list-style: none;">3. le passeport parvenu à expiration, accompagné de l’acte de naissance du père ou de la mère, ou à défaut, l’acte de décès de l’un des deux parents, lorsqu’il s’agit d’une demande de renouvellement.</li>
                                    <li style="list-style: none;">4. le certificat de résidence datant de moins de six (6) mois.</li>
                                    <li style="list-style: none;">5. le certificat de scolarité pour les mineurs scolarisés.</li>
                                    <li style="list-style: none;">6. quatre (4) photos d’identité (le fond de la photo biométrique doit être uni, sans contour et de couleur blanche) en couleur, numériques, récentes et identiques.</li>
                                    <li style="list-style: none;">7. une quittance fiscale ou timbre fiscal dont le montant correspond à la nature du document demandé.</li>
                                    <li style="list-style: none;">8. la copie de la carte du groupe sanguin.</li>
                                </ul>


                                <br><br><b>Pour les demandeurs majeurs résidant à l'étranger :</b>
                                <ul>
                                    <li style="list-style: none;">1. l’extrait d’acte de naissance spécial n°12-S de l’intéressé, délivré sur imprimé spécial.</li>
                                    <li style="list-style: none;">2. la carte d’immatriculation consulaire en cours de validité.</li>
                                    <li style="list-style: none;">3. le justificatif de séjour à l’étranger.</li>
                                    <li style="list-style: none;">4. l’attestation de travail ou le certificat de scolarité pour les étudiants.</li>
                                    <li style="list-style: none;">5. quatre (4) photos d’identité (le fond de la photo biométrique doit être uni, sans contour et de couleur blanche) en couleur, numériques, récentes et identiques.</li>
                                    <li style="list-style: none;">6. une quittance fiscale ou timbre fiscal dont le montant correspond à la nature du document demandé;</li>
                                    <li style="list-style: none;">En cas de renouvellement, il est joint au dossier le passeport parvenu à expiration ou la déclaration de perte ou de vol.</li>
                                </ul>
                                <br><b>Pour les demandeurs mineurs résidant à l'étranger :</b>
                                <ul>
                                    <li style="list-style: none;">1. l’extrait d’acte de naissance spécial n°12-S de l’intéressé, délivré sur imprimé spécial.</li>
                                    <li style="list-style: none;">2. la carte d’immatriculation consulaire en cours de validité.</li>
                                    <li style="list-style: none;">3. le justificatif de séjour à l’étranger.</li>
                                    <li style="list-style: none;">4. le certificat de scolarité pour les mineurs scolarisés.</li>
                                    <li style="list-style: none;">5. quatre (4) photos d’identité (le fond de la photo biométrique doit être uni, sans contour et de couleur blanche) en couleur, numériques, récentes et identiques.</li>
                                    <li style="list-style: none;">6. une quittance fiscale ou timbre fiscal dont le montant correspond à la nature du document demandé;</li>
                                </ul><br><br>
                                <div class="alert alert-info">En cas de renouvellement, le passeport parvenu à expiration ou la déclaration de perte, de détérioration ou de vol est joint au dossier.</div>
                                <div class="alert alert-success"><b style="font-size : 17pt">Demande en ligne</b>
