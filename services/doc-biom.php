                        <h1>Carte d'identité biométrique</h1><br><br>
                        <section>
                            <b style="font-size : 18pt">Le dossier de la première demande de la carte nationale d’identité</b><br><br>
                            <b> Demandeur résidant en Algérie : </b><br>
                            <ul>
                                <li style="list-style : none">1. Un certificat de nationalité algérienne.</li>
                                <li style="list-style : none">2. Un certificat de résidence en cours de validité.</li>
                                <li style="list-style : none">3. Deux(2) photos d’identité en couleur, numériques, récentes et identiques (le fond de la photo doit être uni, sans contour et de couleur blanche).</li>
                            </ul>
                            <br><br>
                            <b>Demandeur résidant à l'étrnager : </b><br>
                            <ul>
                                <li style="list-style : none">1. La carte d’immatriculation consulaire en cours de validité.</li>
                                <li style="list-style : none">2. un certificat de nationalité.</li>
                                <li style="list-style : none">3. Deux(2) photos d’identité (le fond de la photo doit être uni, sans contour et de couleur blanche) en couleur, numériques, récentes et identiques.</li>
                            </ul>
                            <div class="alert alert-info">

                                <ul>
                                    <li style="list-style : circle">Il n'y a pas de condition d’âge pour la délivrance de la carte nationale d’identité.</li>
                                    <li style="list-style : circle">La durée de validité de la carte nationale d’identité est de dix (10) ans à compter de la date de signature.</li>
                                </ul>
                            </div>
                            </p><br><br>
                            <b style="font-size : 18pt">Le dossier de renouvellement de la carte nationale d’identité</b><br><br>
                            <b>1. Cas de renouvellement : </b><br>
                            <ul>
                                <li style="list-style: circle;">Expiration de la durée de validité.</li>
                                <li style="list-style: circle;">Perte ou vol de la carte nationale d’identité.</li>
                                <li style="list-style: circle;">Détérioration de la carte nationale d’identité.</li>
                                <li style="list-style: circle;">Changement de la situation familiale.</li>
                                <li style="list-style: circle;">Changement de lieu de résidence.</li>
                            </ul><br><br>
                            <b>2. Dossier de renouvellement : </b><br>
                            <ul>
                                <li style="list-style: none;">1. l’extrait d’acte de naissance n°12 de l’intéressé.</li>
                                <li style="list-style: none;">2. la carte nationale d’identité parvenue à expiration, accompagnée de l’acte de naissance du père ou de la mère, ou à défaut, l’acte de décès de l’un des deux parents.</li>
                                <li style="list-style: none;"> 3. Le certificat de résidence datant de moins de six (6) mois.</li>
                                <li style="list-style: none;">4. La fiche familiale d’état civil pour les mariés.</li>
                                <li style="list-style: none;">5. Quatre (4) photos d’identité en couleur, numériques, récentes et identiques.</li>
                                <li style="list-style: none;">6. Une quittance fiscale ou timbre fiscal dont le montant correspond à la nature du document demandé.</li>
                                <li style="list-style: none;">7. La copie de la carte du groupe sanguin.</li>
                            </ul><br><br>
